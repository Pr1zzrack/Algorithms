def linear_search(arr, x):
    for i in range(len(arr)):
        if arr[i] == x:
            return i
    return -1

# Пример использования
arr = [64, 34, 25, 12, 22, 11, 90]
x = 22
result = linear_search(arr, x)

if result != -1:
    print(f"Элемент {x} найден на позиции {result}.")
else:
    print(f"Элемент {x} не найден.")


# Объяснение:

# def linear_search(arr, x):: Объявление функции линейного поиска с параметрами arr (массив) и x (искомый элемент).
# for i in range(len(arr)):: Цикл, который проходит по индексам элементов массива.
# if arr[i] == x:: Условие проверки, совпадает ли текущий элемент с искомым элементом.
# return i: Если элемент найден, возвращается его индекс.
# return -1: Если цикл завершается, и элемент не найден, возвращается -1.